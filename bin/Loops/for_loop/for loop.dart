// 1. print 1 to 10 using for loop

 void task1 () {
  for (int loop = 1; loop <10 ; loop = loop+1){
    print(loop);
  }
}

// 2. print the multiplication table of 5 using for loop

void task2 () {
  for (int loop = 1; loop<=10; loop= loop+1){
    print(loop*5);
  }
}




void main () {
  task1();
  task2();
}
